@tree.command(name="poll", description="投票を作成します")
@app_commands.describe(start="最初の時刻", number="<任意> 選択肢の数(2-10)(デフォルトは10)")
@app_commands.choices(start=START_CHOICES, number=NUMS)
async def poll(interaction: discord.Interaction, start: app_commands.Choice[int], number: app_commands.Choice[int] = None):

    num = number.value if number else 10

    now = datetime.now()

    # 締め切り = 選択肢の最後の時間
    target = now.replace(hour=start.value, minute=0, second=0, microsecond=0)
    if target <= now:
        target += timedelta(days=1)
    target += timedelta(hours=num)  # 最後の時間まで加算

    dt = target - now

    poll_obj = discord.Poll(
        question="参加できる時間帯",
        duration=dt,
        multiple=True,
    )
    for i in range(num):
        hour = (start.value + i) % 24
        next_hour = (start.value + i + 1) % 24
        poll_obj.add_answer(text=f"{hour}:00 ～ {next_hour}:00")

    await interaction.response.send_message(poll=poll_obj)

@tree.command(name="randwep", description="VCの参加者それぞれにランダムなブキを指定します  (このコマンドはVC内のチャットにて行ってください)")
@app_commands.describe(weapontype="<任意> ブキのタイプ・サブ・スペシャルを指定します")
async def randwep(interaction: discord.Interaction, weapontype: str = None):
    if not isinstance(interaction.channel, discord.VoiceChannel):
        await interaction.response.send_message("VCのテキストチャンネルから実行してください", ephemeral=True)
        return

    members = interaction.channel.members

    if not members:
        await interaction.response.send_message("VCに誰もいません", ephemeral=True)
        return

    # バリデーション
    valid_values = [val for _, val in all_items]
    if weapontype is not None and weapontype not in valid_values:
        await interaction.response.send_message("無効な入力です", ephemeral=True)
        return

    if weapontype is None:
        msg = ''
        for m in members:
            msg += f'{m.mention}   ->   {random.choice(list(wep.keys()))}\n'
        msg = msg[:-1]
    else:
        msg = ''
        for m in members:
            msg += f'{m.mention}   ->   {random.choice(wepclass[weapontype])}\n'
        msg = msg[:-1]

    await interaction.response.send_message(msg, view=MyView(weapontype=weapontype, members=members))

